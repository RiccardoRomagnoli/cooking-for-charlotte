package it.unibo.oop18.cfc.GameState;

/**
 * Different states of the game.
 */
public enum GameStates {

    /**
     * Fading image intro.
     */
    INTRO,
    /**
     * Menu.
     */
    MENU,
    /**
     * Game state during the game.
     */
    PLAY,
    /**
     * State when the match is over.
     */
    GAMEOVER,
    /**
     * Pause state.
     */
    PAUSE,
    /**
     * Info state.
     */
    INFO,
    /**
     * Option state.
     */
    OPTION
}
